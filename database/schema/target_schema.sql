-- One-time tokens for first-time password setup and password resets.
-- Only the token hash is stored; the emailed link carries the plaintext.
CREATE TABLE IF NOT EXISTS access.password_reset_token (
    token_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id integer NOT NULL REFERENCES access."user"(user_id) ON DELETE CASCADE,
    token_hash varchar(128) NOT NULL UNIQUE,
    expires_at timestamptz NOT NULL,
    used_at timestamptz,
    created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS password_reset_token_user_idx ON access.password_reset_token (user_id);

-- Browser refresh session tokens for the local password sign-in flow.
-- Only token hashes are stored; the browser holds the plaintext token.
CREATE TABLE IF NOT EXISTS access.user_login_session (
    session_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id integer NOT NULL REFERENCES access."user"(user_id) ON DELETE CASCADE,
    token_hash varchar(128) NOT NULL UNIQUE,
    expires_at timestamptz NOT NULL,
    last_seen_at timestamptz,
    revoked_at timestamptz,
    created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS user_login_session_user_idx ON access.user_login_session (user_id);
CREATE INDEX IF NOT EXISTS user_login_session_active_idx ON access.user_login_session (token_hash, expires_at) WHERE revoked_at IS NULL;

CREATE TABLE IF NOT EXISTS planning.plan_cycle (
    cycle_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fiscal_year integer NOT NULL UNIQUE,
    summer_open date NOT NULL,
    summer_close date NOT NULL,
    fall_open date,
    fall_close date,
    cycle_status varchar(20) NOT NULL CHECK (cycle_status IN (
        'Upcoming', 'SummerOpen', 'SummerClosed', 'FallOpen', 'FallClosed', 'Complete'
    )),
    created_by integer NOT NULL REFERENCES access."user"(user_id),
    CHECK (summer_close >= summer_open),
    CHECK (fall_close IS NULL OR fall_open IS NULL OR fall_close >= fall_open)
);

