DO $$
DECLARE
    table_record record;
    table_name_text text;
    modified_by_index text;
BEGIN
    FOR table_record IN
        SELECT table_schema, table_name
        FROM information_schema.tables
        WHERE table_type = 'BASE TABLE'
          AND table_schema IN ('reference', 'access', 'planning', 'performance', 'budget', 'review', 'workflow', 'amendment', 'output')
    LOOP
        table_name_text := format('%I.%I', table_record.table_schema, table_record.table_name);

        IF NOT EXISTS (
            SELECT 1 FROM information_schema.columns
            WHERE table_schema = table_record.table_schema
              AND table_name = table_record.table_name
              AND column_name = 'created_at'
        ) THEN
            EXECUTE format('ALTER TABLE %s ADD COLUMN created_at timestamptz NOT NULL DEFAULT now()', table_name_text);
        END IF;

        IF NOT EXISTS (
            SELECT 1 FROM information_schema.columns
            WHERE table_schema = table_record.table_schema
              AND table_name = table_record.table_name
              AND column_name = 'updated_at'
        ) THEN
            EXECUTE format('ALTER TABLE %s ADD COLUMN updated_at timestamptz NOT NULL DEFAULT now()', table_name_text);
        END IF;

        IF NOT EXISTS (
            SELECT 1 FROM information_schema.columns
            WHERE table_schema = table_record.table_schema
              AND table_name = table_record.table_name
              AND column_name = 'modified_by'
        ) THEN
            EXECUTE format('ALTER TABLE %s ADD COLUMN modified_by integer', table_name_text);
        END IF;

        modified_by_index := format('idx_%s_%s_modified_by', table_record.table_schema, table_record.table_name);
        EXECUTE format('CREATE INDEX IF NOT EXISTS %I ON %s(modified_by)', modified_by_index, table_name_text);
    END LOOP;
END $$;

