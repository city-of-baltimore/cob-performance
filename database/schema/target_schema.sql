-- Target Postgres schema for the Agency Performance & Budget Portal.
-- Adapted from Target_Database_Schema.pdf (Azure SQL, May 2026).
-- This file creates the durable app schema. Dummy data should be loaded separately.
--
-- This file only runs automatically on a fresh database (Postgres
-- docker-entrypoint-initdb.d on first init, or a manual psql -f per
-- DEPLOY.md/database/README.md) -- it is not reapplied on every deploy.
-- R/database.R therefore also creates workflow.plan_approval_stamp,
-- workflow.entity_role_assignment, and application.feedback_request
-- idempotently (CREATE TABLE IF NOT EXISTS) at app startup, so those
-- tables reach already-provisioned databases too. Keep both definitions
-- in sync if these tables change.

